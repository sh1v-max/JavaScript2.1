console.log("Do-While Loop");

let i = 0

console.log(`============do-while================`);
do {
    console.log(i);
    i++
} while (i<5);
 
console.log(`============while================`);


while (i<10) {
    console.log(i);
    i++
}

console.log(`============for================`);
for (let i = 0; i < 5; i++) {
    console.log(i);
}

